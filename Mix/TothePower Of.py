'''
Create a function that takes a
base number and an exponent number and returns the calculation.
'''

def calculate_exponent(num1, exponent):
    """
    :param num1:
    :param exponent:
    :return: ** exponent
    """
    return num1 ** exponent

print(calculate_exponent(5,5))
print(calculate_exponent(10,10))
print(calculate_exponent(3,3))